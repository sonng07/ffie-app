// Document-library content — the full FFIE documents catalogue, cloned 1:1 from
// the live FFIE documents page (https://www.ffie.fr/les-documents-de-la-ffie),
// per WBS Epic 3 / FFIE-DOC-01: "synchronization of existing documents from the
// FFIE site, structuring of the document library."
//
// This is the COMPLETE corpus the site exposes — every document across all 28
// listing pages — not a hand-picked seed. Titles/references are kept verbatim in
// French (the source corpus is French). Production replaces this static export
// with a live sync from the FFIE backend + a local SQLite/MMKV cache; the data
// SHAPE here is what that sync should produce.
//
// Each doc carries:
//   - `family` / `category`: the FFIE site's own two-level taxonomy, so the
//     library can be STRUCTURED and filtered/searched by category (FFIE-DOC-04).
//     `family` is the primary section; `categories` is the full set of facet tags
//     the site files the document under (a document can sit under several).
//   - `memberOnly`: mirrors "Contenu réservé aux adhérents" on the site. Of the
//     full corpus exactly ONE document is public (the electrification plan); the
//     Library tab is member-gated anyway, so this is informational/forward-looking.
//   - `saved`: the DEVICE offline state — is this document downloaded to keep
//     locally? The visible surface of FFIE-DOC-03 ("download documents to keep
//     locally") and Principle 2 (offline-first). A simple two-state flag. Runtime
//     sets this at sync time; the handful marked here are a plausible preview state.
//   - `thumbUrl`: the real FFIE cover image (rendered with a mock-page fallback).
//   - `detailUrl`: the real FFIE detail page for the document.
//   - `sourceUrl`: the real FFIE file URL where one is publicly resolvable.
//
// Some documents on the FFIE site are filed under NO facet category — we keep
// them faithfully under an "Autres documents" section rather than inventing a
// classification (repo rule: no fabricated real-world data).

// Top-level groups, mirroring the family filters on the FFIE documents page, in
// the site's own order. "Autres documents" is the catch-all for untagged docs.
// Order here is the canonical display order in the library.
export const DOC_FAMILIES = [
  "Courants forts",
  "Sûreté / Sécurité Incendie",
  "Vie de l'entreprise",
  "Bâtiments connectés",
  "Performance énergétique",
  "Maintenance",
  "Types de documents",
  "Autres documents",
] as const;

export type DocFamily = (typeof DOC_FAMILIES)[number];

export type Doc = {
  id: number;
  title: string;
  /** FFIE primary sub-category (e.g. "Réseaux de communication"). */
  category: string;
  /** Every FFIE facet the document is filed under (may span families). */
  categories: string[];
  /** FFIE top-level group — used to section the library. */
  family: DocFamily;
  /** "Contenu réservé aux adhérents" on the FFIE site. */
  memberOnly: boolean;
  /** Downloaded to keep locally (FFIE-DOC-03 / P2). Two states only. */
  saved: boolean;
  /** Real FFIE cover image URL (rendered over a mock-page fallback). */
  thumbUrl?: string;
  /** Real FFIE detail-page URL for this document. */
  detailUrl: string;
  /** Real FFIE file URL, where publicly resolvable. */
  sourceUrl?: string;
};

// Display subtitle for a document row/detail — the primary facet category, with
// a neutral label for the untagged "Autres documents" bucket (we don't invent a
// category the FFIE site doesn't assign).
export function docSubtitle(d: Doc): string {
  return d.family === "Autres documents" ? "Document FFIE" : d.category;
}

